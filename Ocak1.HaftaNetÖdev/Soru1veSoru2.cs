//Soru2: Bir programın çalışması için temel bileşenler nelerdir? (Derleyici, RAM, CPU vb.)

//Derleyici (Compiler) → Kaynak kodunu makine diline çevirir.
// CPU (İşlemci) → Programın komutlarını işler ve yürütür.
// RAM (Bellek) → Programın çalışma sırasında verileri sakladığı geçici bellek.
// Depolama (SSD/HDD) → Programın dosyalarının ve verilerinin kalıcı olarak saklandığı yer.
// İşletim Sistemi (OS) → Programın donanım ile iletişimini sağlayan ortam (Windows, Linux vb.).